int ans[MAXN];

vector <int> bitti;
vector <int> Merge(vector <int> & a, vector <int> & b) {
    bitti.clear();
    int temp1, temp2;
    int left1, left2;
    left1 = left2 = 0;
    while(left1 < a.size() || left2 < b.size()) {
        temp1= temp2 = 1e9;
        if (left1 < a.size()) temp1 = a[left1];
        if (left2 < b.size()) temp2 = b[left2];
        if (temp1 == temp2) {
        	left1++;
        	continue;
        }
        if (temp1 < temp2) {
            bitti.push_back(temp1);
            left1++;
        } else {
            bitti.push_back(temp2);
            left2++;
        }
    }
    return bitti;
}


vector <int> T[MAXN];

void dfs (ll v) {
    vector<int> tmp;
    if (a[v] != 0) tmp.pb(a[v]);
    for (int i = 0; i < g[v].size(); i++) {
        ll to = g[v][i];
        dfs(to);
        tmp = Merge(tmp, T[to]);
    }
    ans[v] = tmp.size();
    T[v] = tmp;
    return tmp;
}


